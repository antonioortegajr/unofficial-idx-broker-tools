<h3><a href="site-analyzer">Site Analyzer</a></h3>
<p>When evaluating a web page there are many factors to take into consideration. How search engines may view being one of the biggest. Some automization can make this evealuation quicker and even self serve.</p>
<p>This tool does the following:
<ul>
<li>Mobile friendliness</li>
<li>Google page Speed score</li>
<li>Checks for iframes</li>
<li>Creates link to check for number of indexed pages</li>
</ul>
</p>
<h3><a href="api-quick-tester">API Quick Tester</a></h3>
  <p>Making multiple API calls takes time to copy and paste the example code you need. This tool has all the methods and version along with data about the call itself. GET calls only.
  </p>
  <p>This tool does the following:
  <ul>
  <li>Makes GET calls to the IDX broker API</li>
  <li>Returns data on the API call made</li>
  </ul>
  </p>
<h3><a href="advanced-fields">Find Advacned Fields</a></h3>
<p>trying to find an advanced feild in an MLS data set can be cumbersome. Instead this tool uses the API to bring all adnaced fields and creates a link to a list of field values. Each click on a field values link is an API call so be midful of rate API limits.
  <p>
<p>This tool does the following:
<ul>
<li>Tired of asking if a feild is in a MLS data set? Makes GET calls to the IDX broker API for advanced fields in a MLS</li>
<li>Links all advanced feilds returned to searchfieldvalues GET call to return values in data set</li>
</ul>
